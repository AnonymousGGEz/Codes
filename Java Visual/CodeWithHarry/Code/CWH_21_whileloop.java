public class CWH_21_whileloop {
    public static void main(String[] args) {
        System.out.println(1);
        System.out.println(2);
        System.out.println(3);

        System.out.println("While loop: ");
        int i = 100;
        while (i<=200){
            System.out.println(i);
            i++;
        }
        System.out.println("End");
    }
}
